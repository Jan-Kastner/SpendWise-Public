namespace SpendWise.Common.Enums
{
    /// <summary>
    /// Represents the different themes that can be applied to the application.
    /// </summary>
    public enum Theme
    {
        /// <summary>
        /// The light theme with a bright background and dark text.
        /// </summary>
        Light,

        /// <summary>
        /// The dark theme with a dark background and light text.
        /// </summary>
        Dark,

        /// <summary>
        /// The system default theme, which adapts to the user's system settings.
        /// </summary>
        SystemDefault
    }
}
